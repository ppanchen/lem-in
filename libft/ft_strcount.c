#include "libft.h"

int		ft_strcount(char const *s)
{
	int i;
	int j;

	i = 0;
	j = 1;
	if (!s)
		return (0);
	while (s[i])
	{
		if (s[i] == '\n')
			j++;
		i++;
	}
	return (j);
}
